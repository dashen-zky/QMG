    public function insertRecord($formData)
    {
        if(empty($formData)) {
            return false;
        }

        $transaction = Yii::$app->db->beginTransaction();
        try {
            $editor_uuid_list = explode(',', $formData['editor_uuid_list']);
            foreach($editor_uuid_list as $editor_uuid) {
                $this->insertSingleRecord([
                    'editor_uuid'=>$editor_uuid,
                    'regulation_uuid'=>$formData['regulation_uuid'],
                ]);
            }
        } catch (Exception $e) {
            $transaction->rollBack();
            return false;
        }

        $transaction->commit();
        return true;
    }

    public function updateRecord($formData)
    {
        if(empty($formData)) {
            return false;
        }

        $records = self::find()->select(['editor_uuid'])
            ->andWhere(['regulation_uuid'=>$formData['regulation_uuid']])->asArray()->all();
        $old = $this->getAppointedValue($records, 'editor_uuid');
        if(empty($old) && empty($formData['editor_uuid'])) {
            return true;
        }
        $new = explode(',', $formData['editor_uuid']);
        $shouldInsert = array_diff($new, $old);
        $shouldDelete = array_diff($old, $new);
        $transaction = Yii::$app->db->beginTransaction();
        try {
            foreach($shouldDelete as $uuid) {
                $this->deleteAll([
                    'regulation_uuid'=>$formData['regulation_uuid'],
                    'editor_uuid'=>$uuid
                ]);
            }

            foreach($shouldInsert as $uuid) {
                $this->insertSingleRecord([
                    'regulation_uuid'=>$formData['regulation_uuid'],
                    'editor_uuid'=>$uuid,
                ]);
            }
        } catch (Exception $e) {
            $transaction->rollBack();
            throw $e;
            return false;
        }
        $transaction->commit();
        return true;
    }

    // 根据规章删除元素
    public function deleteRecord($uuid)
    {
        if(empty($uuid)) {
            return false;
        }

        return $this->deleteAll(['regulation_uuid'=>$uuid]);
    }
}